const EmojiPicker: React.FC<EmojiPickerProps> = ({ onEmojiSelect, onClose }) => {
  const [selectedCategory, setSelectedCategory] = useState('Smileys & Emotion');
  const [searchQuery, setSearchQuery] = useState('');
  const [filteredEmojis, setFilteredEmojis] = useState<string[]>([]);
  const pickerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target as Node)) {
        onClose();
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  useEffect(() => {
    if (searchQuery) {
      const allEmojis = Object.values(emojiCategories).flat();
      const filtered = allEmojis.filter(emoji => 
        emoji.includes(searchQuery) || 
        emoji.charCodeAt(0).toString(16).includes(searchQuery.toLowerCase())
      );
      setFilteredEmojis(filtered);
    } else {
      setFilteredEmojis([]);
    }
  }, [searchQuery]);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  const currentEmojis = searchQuery ? filteredEmojis : emojiCategories[selectedCategory as keyof typeof emojiCategories] || [];

  return (
    <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
      <div 
        ref={pickerRef}
        className="bg-surface border border-border rounded-lg p-4 max-w-md w-full mx-4 max-h-96 overflow-hidden"
      >
        <div className="flex items-center justify-between mb-4">
          <h3 className="text-lg font-semibold text-text">Emoji Picker</h3>
          <button
            onClick={onClose}
            className="p-1 hover:bg-hover rounded transition-colors"
          >
            <X className="w-5 h-5 text-text-secondary" />
          </button>
        </div>

        {/* Search */}
        <div className="relative mb-4">
          <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 w-4 h-4 text-text-secondary" />
          <input
            type="text"
            placeholder="Search emojis..."
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            className="w-full pl-10 pr-4 py-2 bg-surface border border-border rounded-lg text-text placeholder-text-secondary focus:outline-none focus:ring-2 focus:ring-primary"
          />
        </div>

        {/* Categories */}
        {!searchQuery && (
          <div className="flex gap-1 mb-4 overflow-x-auto pb-2">
            {Object.keys(emojiCategories).map((category) => (
              <button
                key={category}
                onClick={() => setSelectedCategory(category)}
                className={`px-3 py-1 rounded-full text-xs whitespace-nowrap transition-colors ${
                  selectedCategory === category
                    ? 'bg-primary text-white'
                    : 'bg-hover text-text-secondary hover:text-text'
                }`}
              >
                {category}
              </button>
            ))}
          </div>
        )}

        {/* Emojis Grid */}
        <div className="grid grid-cols-8 gap-2 max-h-64 overflow-y-auto">
          {currentEmojis.map((emoji, index) => (
            <button
              key={`${emoji}-${index}`}
              onClick={() => handleEmojiClick(emoji)}
              className="w-10 h-10 flex items-center justify-center text-xl hover:bg-hover rounded transition-colors"
            >
              {emoji}
            </button>
          ))}
        </div>

        {searchQuery && currentEmojis.length === 0 && (
          <p className="text-center text-text-secondary py-4">
            No emojis found
          </p>
        )}
      </div>
    </div>
  );
};

export default EmojiPicker;
